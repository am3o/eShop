package de.hska.iwi.microservice.category.domian;

import de.hska.iwi.microservice.category.entity.Category;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * Created by ameo on 27.05.16.
 */
@Entity
@Table(name = "category")
public class CategoryDAO implements Serializable {

  private static final long serialVersionUID = 1L;

  @Id
  @GeneratedValue(strategy = GenerationType.IDENTITY)
  @Column(name = "id", nullable = false)
  private int id;

  @Column(name = "name")
  private String name;

  protected CategoryDAO() {
  }

  public CategoryDAO(int id, String name) {
    this.id = id;
    this.name = name;
  }

  public void setId(int id) {
    this.id = id;
  }

  public int getId() {
    return id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  @Override
  public String toString() {
    return String.format("Category[id=%d, name=%s]", this.id, this.name);
  }
}
